using System;
using System.Collections.Generic;
using System.Text;

namespace SimpleCalculatorTDD
{
    public class SimpleCalculatorTDDClass
    {
        public int GetSum(int a, int b)
        {
            return a + b;
        }
        public int GetSubstract(int a, int b)
        {
            return a - b;
        }
        public int GetMultiply(int a, int b)
        {
            return a * b;
        }

        public int GetDivide(int a, int b)
        {
            return a / b;
        }
    }
}
